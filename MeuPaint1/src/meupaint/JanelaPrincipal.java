    private void painelDesenhoMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_painelDesenhoMouseReleased
        
        Forma novaForma = null;
        
        if ( btnLinha.isSelected() ) {
            novaForma = new Linha( xPress, yPress, evt.getX(), evt.getY(), btnCor1.getForeground() );
        } else if ( btnRetangulo.isSelected() ) {
            novaForma = new Retangulo( xPress, yPress, evt.getX(), evt.getY(), btnCor1.getForeground(), btnCor2.getForeground() );
        } else if ( btnElipse.isSelected() ){
            novaForma = new Elipse( xPress, yPress, evt.getX(), evt.getY(), btnCor1.getForeground(), btnCor2.getForeground() );
        } else if (btnPoligono.isSelected()) {
            int lados = Integer.parseInt(txtLados.getText()); 
            novaForma = new Poligono(xPress, yPress, evt.getX(), evt.getY(), lados, btnCor1.getForeground(), btnCor2.getForeground());
        }
        
        painelDesenho.adicionarForma( novaForma );
        painelDesenho.setFormaTemporaria( null );
        painelDesenho.repaint();
        
    }//GEN-LAST:event_painelDesenhoMouseReleased

    private void painelDesenhoMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_painelDesenhoMouseDragged
        
        Forma formaTemp = null;
        
        if ( btnLinha.isSelected() ) {
            formaTemp = new Linha( xPress, yPress, evt.getX(), evt.getY(), btnCor1.getForeground() );
        } else if ( btnRetangulo.isSelected() ) {
            formaTemp = new Retangulo( xPress, yPress, evt.getX(), evt.getY(), btnCor1.getForeground(), btnCor2.getForeground() );
        } else if (btnElipse.isSelected() ){
            formaTemp = new Elipse( xPress, yPress, evt.getX(), evt.getY(), btnCor1.getForeground(), btnCor2.getForeground() );
        } else if (btnPoligono.isSelected()) {
            int lados = Integer.parseInt(txtLados.getText()); // N√∫mero de lados do pol√≠gono a partir de um campo de texto
            formaTemp = new Poligono(xPress, yPress, evt.getX(), evt.getY(), lados, btnCor1.getForeground(), btnCor2.getForeground());
        }
        
        painelDesenho.setFormaTemporaria( formaTemp );
        painelDesenho.repaint();
        
    }//GEN-LAST:event_painelDesenhoMouseDragged

